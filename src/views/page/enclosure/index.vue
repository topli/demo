<template>
  <div class="list-template">
    <search-tem class="list-search" @on-search="onSearch">
      <el-form :inline="true" :model="searchFrom">
        <el-form-item>
          <el-input v-model="searchFrom.user" :placeholder="$t('user.username')" clearable/>
        </el-form-item>
      </el-form>
    </search-tem>
    <div class="btns">
      <icon-btn :content="$t('app.add')" auth-code="add" icon="add" @click="editData"/>
      <icon-btn :content="$t('app.import')" auth-code="import" icon="import" @click="importFun"/>
      <icon-btn :content="$t('app.export')" auth-code="export" icon="export" @click="exportFun"/>
    </div>
    <div class="table">
      <t-for-col
        :data="list"
        :sort-change="sortChange"
        :columns-title="columnsTitle"
        :loading="loading"
        selection
        index
        @select-change="handleSelectionChange"/>
    </div>
    <div class="pages">
      <el-pagination
        :current-page="searchData.pageNo"
        :page-sizes="pageSizeOpts"
        :page-size="searchData.pageSize"
        :total="totalElement"
        layout="total, sizes, prev, pager, next, jumper"
        @size-change="handleSizeChange"
        @current-change="handleCurrentChange"/>
    </div>
  </div>
</template>

<script>
import add from './add'
import list from '@/libs/mixins/list'
import dialog from '@/libs/mixins/dialog'
import { getList, delData } from './service'

export default {
  mixins: [list, dialog],
  data() {
    return {
      columnsTitle: [
        {
          key: 'status',
          title: this.$t('enclosure.status'),
          filters: 'status', // 带过滤器的项 取值是时前面加上 _f_
          width: '120',
          render: (h, params) => {
            return h('el-tag', {
              props: { color: params.row.status ? '#52c08e' : '#EE3B3B', type: 'text' },
              style: { color: 'white' }
            },
            params.row.status ? '启用' : '禁用')
          }
        },
        {
          key: 'enclosureName',
          title: this.$t('enclosure.enclosureName'),
          width: '180'
        },
        {
          key: 'enclosureType',
          title: this.$t('enclosure.enclosureType'),
          width: '120'
        },
        {
          key: 'org',
          title: this.$t('enclosure.org'),
          width: '150'
        },
        {
          key: 'province',
          title: this.$t('enclosure.province'),
          width: '130'
        },
        {
          key: 'city',
          title: this.$t('enclosure.city'),
          width: '130'
        },
        {
          key: 'adress',
          title: this.$t('enclosure.adress'),
          width: '250'
        },
        {
          key: 'enclosureRange',
          title: this.$t('enclosure.enclosureRange'),
          width: '100',
          unit: this.$t('enclosure.rangeUnit')
        },
        {
          key: 'createTime',
          title: this.$t('enclosure.createTime'),
          width: '150',
          filters: 'parseTime'
        },
        {
          title: this.$t('app.buttons'),
          width: '100',
          align: 'center',
          render: (h, params) => {
            return h('div', this.iconBtn(h, params, [
              { icon: 'edit', t: 'app.modify', handler: this.editData, color: '#F6BD30' },
              { icon: 'delete', t: 'app.delete', handler: this.deleteItem, color: '#F24D5D' }
            ]))
          }
        }
      ],
      fileType: 'user'
    }
  },
  mounted() {
  },
  methods: {
    sortChange(data) {
      console.log(data)
    },
    _getList() {
      this.loading = true
      getList(this.searchData).then(res => {
        setTimeout(() => {
          this.loading = false
          this.list = res.data.list
          this.totalElement = res.data.total
        }, 1000)
      })
    },
    // 在编辑时获取当前数据
    getFormById(id) {
      setTimeout(() => {
        this.list.forEach((item) => {
          if (item.id === id) this.dialogData = item
        })
      }, 1000)
    },
    editData(row) {
      console.log(row)
      this.$router.push({ name: 'enclosureEdit', query: { name: row.adress, scope: row.enclosureRange }})
    },
    addData() {
      this.$dialogBox({
        title: this.$t('app.add'),
        components: add,
        width: 650,
        onSub: (el) => {
          // 新增完成后执行操作
          // todo 刷新列表
          this._getList()
        }
      })
    },
    deleteItem(row) {
      this.confirm((success) => {
        delData(row).then((res) => {
          console.log(res)
          if (res.code === 200) {
            this.$message({
              type: 'success',
              message: this.$t('app.delete') + this.$t('app.success')
            })
          } else {
            this.$message({
              type: 'error',
              message: res.message
            })
            return
          }
          this._getList()
        })
      })
    }
  }
}
</script>

