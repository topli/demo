<style lang="scss" scoped>
  .echarts-report {
    position: relative;
  }
</style>
<template>
  <div :style="{height:height + 'px',width:width}"/>
</template>

<script>
import echarts from 'echarts'
export default {
  props: {
    width: {
      type: String,
      default: '100%'
    },
    height: {
      type: String,
      default: '150'
    },
    reportObject: {
      type: Object,
      default: () => {
        return {}
      }
    }
  },
  data() {
    return {
      chart: null
    }
  },
  computed: {
  },
  watch: {
    reportObject: {
      handler: function(val) {
        this.chart.setOption(val)
      },
      deep: true
    }
  },
  created() {
    window.addEventListener('resize', this.resize)
  },
  mounted() {
    this._initCharts()
    // 监听侧边栏的变化
    const sidebarElm = document.getElementsByClassName('sidebar-container')[0]
    sidebarElm.addEventListener('transitionend', this.resize)
  },
  beforeDestroy() {
    if (!this.chart) {
      return
    }
    this.chart.dispose()
    this.chart = null
    const sidebarElm = document.getElementsByClassName('sidebar-container')[0]
    sidebarElm.removeEventListener('transitionend', this.resize)
    window.removeEventListener('resize', this.resize)
  },
  methods: {
    resize() {
      if ((event.type === 'transitionend' && event.propertyName === 'width') || event.type === 'resize') {
        if (this.chart) {
          setTimeout(() => {
            this.chart.resize()
          }, 200)
        }
      }
    },
    _initCharts() {
      console.log('a')
      this.chart = echarts.init(this.$el)
      this.chart.setOption(this.reportObject)
    }
  }
}
</script>
